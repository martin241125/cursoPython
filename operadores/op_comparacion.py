# operadores de compracion
# == es igual que
# != es distinto de
# < es menor que
# <= es menor que o igual que
# > es mayor que
# >= es mayor que o igual que

# los operadores de comparacion devuelven TRUE O FALSE

igual_que = 5 == 6 # devuelve false porque no son iguales

distinto_de = 5 != 6 # devuelve true porque son distintos

mayor_que = 5 > 6 # devuelve false porque 5 no es mayor que 6

menor_que = 5 < 6 # devuelve true porque 5 si es menor que 6

mayor_o_igual = 5 >= 6 # devuelve false porque 5 no es mayor ni igual que 6

menor_o_igual = 5 <= 6 # devuelve true porque 5 si es menor que 6

print(menor_o_igual)